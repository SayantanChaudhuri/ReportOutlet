package com.sayantan.retail.util;

public class Math {

    public static double add(double a, String b) {
        return a + Double.parseDouble(b);
    }

    public static double minus(double a, String b) {
        return a - Double.parseDouble(b);
    }

    public static double multiply(double a, String b) {
        return a * Double.parseDouble(b);
    }
}
